// Scope variables
//  Dos tipos de declaración de variables han sido agregadas let y const.
// Let es el nuevo var pero mejor ya que su declaración se limita al contexto en el que se declaró.
// Por otro lado, const nos permite crear un valor que no puede ser redeclarado después de inicializarlo.


// Plantillas cadena de texto
// Los template strings o plantillas de texto son una característica que nos permite interpolar expresiones en strings entre muchas cosas más. Para ello en vez de usar comillas simples ' usamos tildes invertidas ` y las expresiones que deseamos poner las colocamos dentro de llaves después de un signo de dólar.


// Arrow functions
// Las arrow functions (arrow function) son una nueva forma de escribir funciones en JavaScript más elegante y sencilla, en especial para funciones cortas. Una arrow function se compone de un par de paréntesis para los parámetros, un símbolo de flecha (=>, signo de igual y luego mayor qué) y llaves: (arg1, arg2) => { // función }.

    
// Destructuring
// hace posible la extracción de datos de arreglos u objetos usando la misma sintaxis utilizada para crearlos.


// Promesas
// Son una nueva forma de crear código asíncrono. Dejando de lado los callbacks,La forma de crear una promesa es con su constructor Promise pasando una función que recibe resolver y rechazar los cuales llamaremos cuando sea apropiado